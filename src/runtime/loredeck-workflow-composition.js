        openDuplicateLoredeckDialog: deps.openDuplicateLoredeckDialog,
        openLoredeckWorkbench: deps.openLoredeckWorkbench,
        openLoredeckCreatorWorkbench: deps.openLoredeckCreatorWorkbench,
        selectLoredeckForDetails: deps.selectLoredeckForDetails,
